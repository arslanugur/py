# In the program below, we take a temperature in degree Celsius and convert it into degree Fahrenheit. 
# They are related by the formula:
# fahrenheit = celsius * 1.8 + 32


# Source Code: Python Program to convert temperature in celsius to fahrenheit
celsius = 37.5  # change this value for a different result

fahrenheit = (celsius * 1.8) + 32 # calculate fahrenheit
print('%0.1f degree Celsius is equal to %0.1f degree Fahrenheit' %(celsius,fahrenheit))


# Output: 37.5 degree Celsius is equal to 99.5 degree Fahrenheit

